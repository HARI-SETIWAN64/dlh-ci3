                      Assalaamu'alaikum Warahmatullahi Wabarakatuh.<br/>

Best wishes to all of us,<br/>
                      The spirit of jenggirat tangi has been the inspiration for all the people of Banyuwangi, for never stop improving to be better. Not only improvements in infrastructure sector, but also excellent service for the society and other multisectoral services, are always be the primary
concern. To strengthen the economic structure in Banyuwangi, it is necessary to do a concrete effort to build a brand image that Banyuwangi is a very decent, worthy regency, to be the prime tourism destination also to be a land of potential investments. <br/>

Banyuwangi Festival Multievents which has been held since 2011 is a routine agenda as a show window to exhibit various natural resources potential, also the beauty and arts riches in this Bumi Blambangan. By summarizing 38 various and attractive events, Banyuwangi does not only accentuate cultural events and existing local potentials, but also maintains some international sport competition events along with many other festivals which are humanistic and directly reach the people's sector, including Festival Bedah Rumah (Houses renovations Festivals), Festival Anak Yatim (The Orphans Festival), Festival Toilet Bersih (Toilet Cleanliness Festival), and Festival Santri (The Islamic Students Festival). <br/>

Banyuwangi Festival is one of the efforts to gain the spirit of togetherness between all components in The Government of Banyuwangi and all vertical agencies, bankings, private sectors, and all the people of Banyuwangi. Sense of belonging which is fostered by the spirit of "I Love Banyuwangi" has been proven effective with the generous active contributions in succeeding this big event. <br/>

Positive synergy that has been developed harmoniously, has brought us to many achievements in the last four years. Not only many appreciations in the form of awards from the central government, but Banyuwangi's economic performance also shows an excellent progress. Increase of Regional Gross Domestic Products, has been raised up to 53.4 trillion rupiahs in 2015, whereas in 2010 it was only 23.29 trillion rupiahs. This achievement had been surpassed the RPJMD's target which is the benchmark of the regional gorvenment's performance. <br/>


The economic growth also shows a positive trend from year to year. In 2010, Banyuwangi's economic growth reach 6 percent. This number increased up to 7.14 percent in 2011 and 7.29 percent in 2012. While in 2013, when the national economic decreased, the Banyuwangi's growth still reached above the economic growth of East Java and national, by 6.85 per cent and 6.94 percent in the end of 2014. <br/>

Banyuwangi's existence with all the dynamics also stimulates investments growth. If in 2011 the number of investments was only 350 billion rupiahs, in 2012, there was a significant increase, it became 1.19 trillion rupiahs. In 2013, it increased become 3.44 trillion rupiahs. Until March 2014, the number of investments entering Banyuwangi have reached 586.57 billion rupiahs. With all the supports from all elements in Banyuwangi Regency, we are optimistic this number will continuously grow to enhance the economic advancement in Banyuwangi. <br/>

Some of the programs in tourism and investments development in Banyuwangi regency, gratefully also directly affects the enhancement of the society's welfare. The income per capita significantly increases up to 70%, from 14.97 million rupiahs, in 2010, became 33.7 million rupiahs in 2015. Income per capita of Banyuwangi has surpassed some of other regencies in East Java, which usually always reached higher value than Banyuwangi.<br/>

All of these achievements are the results of our hardworks, all of us. Therefore, I would like to thank you for all the concerns and supports that have been given by all the vertical agencies and bankings, private sectors and all the people of Banyuwangi. Also thank you for all the sacrifices and hardworks of The Government of Banyuwangi. Insya Allah, these will be the part of our good deeds and be the blessings for all the people in Banyuwangi.<br/>

Including those that contributes for this fourth International Tour de Banyuwangi Ijen 2016, which is possibly held because of all the beneficial supports from many parties. I wish all of us health, and easiness in holding this international event during 11 to 14 May 2016. This success will be our success. The pride will be our motivation to keep creating and innovating, that Banyuwangi's existence would be more recognized in international global world. <br/>

Long life Indonesia, long life Banyuwangi. I hope Allah SWT will always protect us and  shower us with the blessings, Amin yaa robbal alamin.

<br/>
Wallahul muwafiq ila aqwammitthariq<br/>
Wassalamu'alaikum Warahmatullahi Wabarrakatuh.
